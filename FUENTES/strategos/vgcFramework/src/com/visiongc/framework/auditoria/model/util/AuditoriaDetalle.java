package com.visiongc.framework.auditoria.model.util;

public class AuditoriaDetalle {
	
	private Long auditoriaId;
	private String claseNombre;
	private String fecha;
	private String atributo;
	private String valor;
	
	public Long getAuditoriaId() {
		return auditoriaId;
	}
	public void setAuditoriaId(Long auditoriaId) {
		this.auditoriaId = auditoriaId;
	}
	public String getClaseNombre() {
		return claseNombre;
	}
	public void setClaseNombre(String claseNombre) {
		this.claseNombre = claseNombre;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getAtributo() {
		return atributo;
	}
	public void setAtributo(String atributo) {
		this.atributo = atributo;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	
	
	
}
